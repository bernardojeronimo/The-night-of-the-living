package pt.ulusofona.lp2.thenightofthelivingdeisi;

public class Posicao {
    private int x;
    private int y;

    public Posicao(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Posicao() {
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


}
